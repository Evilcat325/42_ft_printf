#include "libft.h"

char	*ft_ltoa(long n)
{
	long	nbr;
	long	d;
	size_t	i;
	char	*str;

	nbr = n < 0 ? -(n + 1) : n;
	d = 1;
	i = 1;
	while (nbr / d >= 10)
	{
		d *= 10;
		i++;
	}
	if (!(str = ft_strnew(i + (n < 0 ? 1 : 0))))
		return (NULL);
	i = 0;
	if (n < 0)
		str[i++] = '-';
	while (d != 0)
	{
		str[i++] = nbr / d % 10 + '0'
			+ (d == 1 && n < 0 && n - 1 > 0 ? 1 : 0);
		d /= 10;
	}
	return (str);
}
